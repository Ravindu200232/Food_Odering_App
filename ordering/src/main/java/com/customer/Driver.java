package com.customer;

public class Driver {

	
	private int did;
	private String dname;
	private String dphone;
	private String demail;
	private String daddress;
	
	public Driver(int did, String dname, String dphone, String demail, String daddress) {
		super();
		this.did = did;
		this.dname = dname;
		this.dphone = dphone;
		this.demail = demail;
		this.daddress = daddress;
	}

	public int getDid() {
		return did;
	}

	public String getDname() {
		return dname;
	}

	public String getDphone() {
		return dphone;
	}

	public String getDemail() {
		return demail;
	}

	public String getDaddress() {
		return daddress;
	}

	
	
	
	


	
	
}
